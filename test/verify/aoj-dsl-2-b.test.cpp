#define PROBLEM \
  "http://judge.u-aizu.ac.jp/onlinejudge/description.jsp?id=DSL_2_B"

#include "../../structure/others/binary-indexed-tree.hpp"
#include "../../template/template.hpp"

int main() {
  int N, Q;
  cin >> N >> Q;
  BinaryIndexedTree< int > bit(N);
  while (Q--) {
    int T, X, Y;
    cin >> T >> X >> Y;
    if (T == 0)
      bit.apply(X - 1, Y);
    else
      cout << bit.prod(X - 1, Y) << "\n";
  }
}
